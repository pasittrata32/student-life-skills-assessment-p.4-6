const createStudentList = (classLevel: string, names: string[]): Student[] => {
  return names.map((fullName, index) => {
    let firstName = fullName;
    let lastName = "";
    
    // Simple parsing logic to attempt to separate Name from Surname
    // Assumes format like "Title Name Surname" or "Title Name Middle Surname"
    // It keeps the Title with the First Name
    const firstSpaceIndex = fullName.indexOf(' ', fullName.indexOf('.') + 1);
    if (firstSpaceIndex !== -1) {
       const secondSpaceIndex = fullName.indexOf(' ', firstSpaceIndex + 1);
       if (secondSpaceIndex !== -1) {
           // If there are multiple spaces, assume the first part after title is First Name
           // and the rest is Last Name (handling middle names or compound surnames)
           firstName = fullName.substring(0, secondSpaceIndex);
           lastName = fullName.substring(secondSpaceIndex + 1);
       } else {
           firstName = fullName.substring(0, firstSpaceIndex);
           lastName = fullName.substring(firstSpaceIndex + 1);
       }
    }

    return {
      id: `s-${classLevel.replace(/[./]/g, '')}-${index + 1}`,
      firstName: firstName,
      lastName: lastName,
      classLevel: classLevel,
      number: (index + 1).toString(),
      isAssessed: false
    };
  });
};

